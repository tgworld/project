package myce.casia.db;

public class ExamationAndKnowledge {
	
	private Integer id;
	private String examation;
	private String knowledge;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getExamation() {
		return examation;
	}
	public void setExamation(String examation) {
		this.examation = examation;
	}
	public String getKnowledge() {
		return knowledge;
	}
	public void setKnowledge(String knowledge) {
		this.knowledge = knowledge;
	}
	
	

}
